export function getDeckById(id: string): Deck | undefined {
  return sampleDecks.find(deck => deck.id === id);
}

export function getFreeDeck(): Deck[] {
  return sampleDecks.filter(deck => !deck.isPremium);
}

export function getPremiumDecks(): Deck[] {
  return sampleDecks.filter(deck => deck.isPremium);
}
